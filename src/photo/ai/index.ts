import { Tags } from '@/tag';
import { parseCommaSeparatedKeyString } from '@/utility/key';
import { z } from 'zod';
import { AI_SUBJECT_TAGS_MAX } from './prompts';
import { TAG_FACETS, slugsForFacet } from './tagVocabulary';

export type AiAutoGeneratedField =
  'title' |
  'caption' |
  'tags' |
  'semantic'

export const AI_AUTO_GENERATED_FIELDS_ALL: AiAutoGeneratedField[] = [
  'title',
  'caption',
  'tags',
  'semantic',
];

export const AI_AUTO_GENERATED_FIELDS_DEFAULT: AiAutoGeneratedField[] = [
  'title',
  'tags',
  'semantic',
];

export const parseAiAutoGeneratedFieldsString = (
  string?: string,
) =>
  parseCommaSeparatedKeyString({
    string,
    acceptedKeys: AI_AUTO_GENERATED_FIELDS_ALL,
    defaultKeys: AI_AUTO_GENERATED_FIELDS_DEFAULT,
  });

export const getAiTextFieldsToGenerate = (
  textFieldsToGenerate: AiAutoGeneratedField[],
  excludeTitle?: boolean,
  excludeCaption?: boolean,
  excludeTags?: boolean,
  excludeSemantic?: boolean,
): AiAutoGeneratedField[] => {
  return textFieldsToGenerate.filter(field =>
    !(excludeTitle && field === 'title') &&
    !(excludeCaption && field === 'caption') &&
    !(excludeTags && field === 'tags') &&
    !(excludeSemantic && field === 'semantic'),
  );
};

export type AiImageQuery =
  'title' |
  'caption' |
  'title-and-caption' |
  'tags' |
  'semantic';

// PLOG-15: controlled-vocabulary classification prompt, built from the single
// source of truth (tagVocabulary). The z.enum schema HARD-enforces membership;
// this text steers WHICH value the model picks and lists the allowed slugs.
const getFacetClassificationPrompt = (): string => {
  const facetLines = TAG_FACETS.map(facet => {
    const values = facet.values.map(v => v.slug).join(', ');
    const optional = facet.required
      ? ''
      : ' (optional — null if there is no distinct light or time of day)';
    return `- ${facet.key} (${facet.zh})${optional}: ${values}`;
  });
  return [
    'Classify the image into these facets. For each facet pick the SINGLE best',
    'value from its allowed list and return the EXACT slug verbatim:',
    ...facetLines,
    `Also return 0-${AI_SUBJECT_TAGS_MAX} free-form "subject" keywords naming`,
    'specific objects, places, or elements in the image (lowercase, 1-2 words,',
    'hyphenated like "red-bicycle"), plus "subject_zh" with their Chinese',
    'aligned position-by-position.',
  ].join('\n');
};

export const getAiImageQuery = (
  query: AiImageQuery,
  existingTitle?: string,
  _existingTags: Tags = [],
): string => {
  switch (query) {
    case 'title': return 'Write a compelling title for this image in 3 words or less.';
    case 'caption': return existingTitle
      ? `Write a pithy caption for this image in 6 words or less and no punctuation that complements the existing title: "${existingTitle}."`
      : 'Write a pithy caption for this image in 6 words or less and no punctuation.';
    case 'title-and-caption': return 'Write a compelling title and pithy caption of 8 words or less for this image, using the format Title: "title" Caption: "caption."';
    case 'tags': return getFacetClassificationPrompt();
    case 'semantic': return 'Describe this image succinctly without initial text like "This image shows" or "This is a picture of."';
  }
};

const getAiImageQueryForField = (
  field: AiAutoGeneratedField,
  existingTitle?: string,
  existingTags?: Tags,
) => {
  switch(field) {
    case 'title': return `TITLE: ${getAiImageQuery('title', existingTitle, existingTags)}`;
    case 'caption': return `CAPTION: ${getAiImageQuery('caption', existingTitle, existingTags)}`;
    case 'tags': return `TAGS: ${getAiImageQuery('tags', existingTitle, existingTags)}`;
    case 'semantic': return `SEMANTIC: ${getAiImageQuery('semantic', existingTitle, existingTags)}`;
  }
};

export const getAiImageQuerySchema = (
  fields: AiAutoGeneratedField[],
  existingTitle?: string,
  existingTags?: Tags,
) => {
  const queryLines = [
    'Generate a set of meta content for the attached image:\n',
  ];

  fields.forEach(field => {
    queryLines.push(getAiImageQueryForField(field, existingTitle, existingTags));
  });

  // FORK: bilingual output for the free-text fields. The facet tags above carry
  // their Chinese in the app (vocabulary-defined), so the model is NOT asked to
  // translate them — only title/caption/semantic and the free-form subjects.
  queryLines.push(
    '\nFor the title, caption and semantic fields, also provide a natural ' +
    'Simplified-Chinese sibling (title_zh, caption_zh, semantic_zh). Translate ' +
    'naturally, not literally. Put ONLY final values in every field — never ' +
    'reasoning, notes, schema talk, or field names inside a value.',
  );

  const query = queryLines.join('\n');

  let schema = z.object();

  // PLOG-9: typed array for tags (CSV string was unenforceable) + length caps
  // on the text fields. min(tags) is enforced by a tolerant retry at the query
  // layer; normalizeAiResult enforces the rest deterministically.
  // FORK: each field gains an OPTIONAL `*_zh` sibling — optional so a photo
  // whose model output omits a translation still validates (display falls back
  // to English) rather than hard-failing a batch item.
  // zh siblings are `.nullable()` (NOT `.optional()`): OpenAI strict structured
  // output requires every property in `required`, so an absent field must be
  // expressed as null, not omitted. The model returns null when it can't
  // translate; normalizeAiResult treats null as absent (display falls back).
  if (fields.includes('title')) {
    schema = schema.extend({
      title: z.string().max(120),
      title_zh: z.string().max(120).nullable(),
    }); }
  if (fields.includes('caption')) {
    schema = schema.extend({
      caption: z.string().max(240),
      caption_zh: z.string().max(240).nullable(),
    }); }
  // PLOG-15: tags become a CONTROLLED facet classification. One z.enum per
  // facet hard-constrains the model to the shared vocabulary (→ clustering);
  // `light` is nullable (not every frame has distinct light/time). `subject`
  // keeps 0-2 free-form keywords for specificity. The facet result is collapsed
  // to the `tags`/`tags_zh` arrays downstream (facetsToTags), so the DB/browse
  // are unchanged.
  if (fields.includes('tags')) {
    TAG_FACETS.forEach(facet => {
      const slugEnum = z.enum(
        slugsForFacet(facet.key) as [string, ...string[]],
      );
      schema = schema.extend({
        [facet.key]: facet.required ? slugEnum : slugEnum.nullable(),
      });
    });
    schema = schema.extend({
      subject: z.array(z.string()).max(AI_SUBJECT_TAGS_MAX),
      subject_zh: z.array(z.string()).max(AI_SUBJECT_TAGS_MAX).nullable(),
    });
  }
  if (fields.includes('semantic')) {
    schema = schema.extend({
      semantic: z.string().max(600),
      semantic_zh: z.string().max(600).nullable(),
    }); }

  return {
    query,
    schema,
  };
};

export const parseTitleAndCaption = (text: string) => {
  const matches = text.includes('Title')
    ? text.match(/^[`'"]*Title: ["']*(.*?)["']*[ ]*Caption: ["']*(.*?)\.*["']*[`'"]*$/)
    : text.match(/^(.*?): (.*?)$/);

  return {
    title: matches?.[1] ?? '',
    caption: matches?.[2] ?? '',
  };
};

export const cleanUpAiTextResponse = (text: string) =>
  text
    .replaceAll('\n', ' ')
    .replaceAll('"', '')
    .replaceAll('**', '')
    .replaceAll('`', '')
    .replace(/\.$/, '');
